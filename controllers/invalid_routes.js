const router = require('express').Router();



//--------------------------------------------------------------------------------------------------------
//  Catch Invalid Routes
//--------------------------------------------------------------------------------------------------------


//  Any other Route (that is not defined)
router.get('*', (req, res) => {
    res.status(202).json({ 'status': '404', 'message': "Invalid Route Called" });
    console.log("Wrong Route Called");
    res.end();
})
